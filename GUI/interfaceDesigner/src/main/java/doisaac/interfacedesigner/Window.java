    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        // TODO add your handling code here:
        /* Save the object selected in the combo and casting it into a Person Object*/
        Person personSelected = (Person) personCombo.getSelectedItem();
        
        // creates some variables that will save the person information
        String name = personSelected.getName();
        String lastName = personSelected.getLastName();
        String email = personSelected.getEmail();
        String direction = personSelected.getDirection();
        String cellPhoneNumber = personSelected.getCellPhoneNumber();
        
        //Creates an array that contains the person information together
        String[] personInformation = {name, lastName, email,cellPhoneNumber, direction};
        
        if (peopleInTable.contains(personSelected)){
            JOptionPane.showMessageDialog(null, "That person already exist, please try again");
        } else {
            peopleInTable.add(personSelected);
            tableModel.addRow(personInformation);
        }
        
    }//GEN-LAST:event_insertButtonActionPerformed

